const translationTable = translations as Record<LanguageCode, Record<string, string>>;

const I18nContext = createContext<I18nContextValue | null>(null);

function isSupportedLanguage(value: string): value is LanguageCode {
  return SUPPORTED_LANGUAGES.some(({ code }) => code === value);
}

function interpolate(template: string, params?: TranslationParams) {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replaceAll(`{${key}}`, String(value));
  }, template);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>(DEFAULT_LANGUAGE);

  useEffect(() => {
    let cancelled = false;

    const loadStoredLanguage = async () => {
      try {
        const storedLanguage = await AsyncStorage.getItem(LANGUAGE_STORAGE_KEY);
        if (!cancelled && storedLanguage && isSupportedLanguage(storedLanguage)) {
          setLanguageState(storedLanguage);
        }
      } catch {
        if (!cancelled) {
          setLanguageState(DEFAULT_LANGUAGE);
        }
      }
    };

    void loadStoredLanguage();

    return () => {
      cancelled = true;
    };
  }, []);

  const setLanguage = useCallback((nextLanguage: LanguageCode) => {
    setLanguageState(nextLanguage);
    void AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage).catch(() => undefined);
  }, []);

  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage,
    languages: SUPPORTED_LANGUAGES,
    t: (key, params) => {
      const template = translationTable[language][key] ?? translationTable.en[key] ?? key;
      return interpolate(template, params);
    },
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }

  return context;
}
